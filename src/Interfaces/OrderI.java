    private void txtCIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCIdKeyReleased
        // Validate Client ID

        String PATTERN="^[C]{1}[0-9]{5}$";
        Pattern patt=Pattern.compile(PATTERN);
        Matcher match=patt.matcher(txtCId.getText());
        if(!match.matches()){
            jLabel14.setText("Client ID is Incorrect!");
        }
        else{
            jLabel14.setText(null);
        }
    }//GEN-LAST:event_txtCIdKeyReleased

