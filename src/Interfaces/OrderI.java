    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // View
        dbReffOrder = FirebaseDatabase.getInstance().getReference().child("Order");

        dbReffOrder.addValueEventListener(new ValueEventListener() {
            @Override
            public void onDataChange(DataSnapshot ds) {
                int i=0;
                System.out.println(ds.getChildrenCount());
                for(DataSnapshot d : ds.getChildren()){
                    orList.add(d.getValue(Order.class));
                    i++;

                    if(i==ds.getChildrenCount()){
                        for(Order s : orList) {
                            if(s.getOrderID().equals(txtId.getText())){
                                String details="Order ID : "+s.getOrderID()+"\n Date : "+s.getDate()+"\n ClientID : "+s.getClientID()+"\n Order Item : "+s.getOrderItem()+"\n Quantity : "+s.getDescription();
                                txtShow1.setText(details);
                                txtId.setText(s.getOrderID());
                                txtDate.setText(s.getDate());
                                txtCId.setText(s.getClientID());
                                txtItem.setText(s.getOrderItem());
                                txtQuantity.setText(Integer.toString(s.getQuantity()));
                                txtDes.setText(s.getDescription());
                            }
                        }
                    }
                }
            }

            @Override
            public void onCancelled(DatabaseError de) {
                throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            }
        });
    }//GEN-LAST:event_jButton9ActionPerformed

